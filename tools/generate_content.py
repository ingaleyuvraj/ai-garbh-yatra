def build_audio_catalog():
    items = []
    counters = {}
    for cat, title, query in AUDIO_CATALOG:
        counters[cat] = counters.get(cat, 0) + 1
        items.append({
            "id": f"aud_{cat}_{counters[cat]:02d}",
            "category": cat,
            "titleMr": title,
            "youtubeId": "",            # optional: fill for in-app embed playback
            "youtubeQuery": query,      # always works: opens official YouTube
            "license": "YouTube",
            "attribution": "YouTube वरील सार्वजनिक सामग्री",
        })
    return items


